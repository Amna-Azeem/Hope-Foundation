    <h2>Our Story</h2>
    <p>
     The HOPE Foundation began with a simple idea — that no life should be lost because of the unavailability of blood. It all started in 2010 when a group of university students came together after witnessing a tragedy in which a young accident victim could not be saved due to a lack of donors. Deeply moved, they pledged to build a platform where people could come forward, learn, and contribute to saving lives through blood donation. 
    </p>
    <p>
      What began as a small campus initiative slowly grew into a community movement. Volunteers from different cities joined in, spreading awareness about the importance of safe and timely blood donations. With every successful donation, the Foundation’s mission became stronger, proving that even one person’s contribution could mean the world to someone in need. 
    </p>
    <p>
